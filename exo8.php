<p> Ecrire un algorithme qui renvoie la table de multiplication d'un nombre passé en paramètre <br>
Proposer 2 solutions avec 2 types de boucles <br>

<h2>Résultat</h2>

<?php

$table=8;
// BOUCLES (loop)
// Afficher les chiffres de 1 à 10

// FOR (pour)
// $i++  -->   $i = $i + 1

for($i = 1; $i <= 10; $i++) {
    echo $i*$table." ";
}




